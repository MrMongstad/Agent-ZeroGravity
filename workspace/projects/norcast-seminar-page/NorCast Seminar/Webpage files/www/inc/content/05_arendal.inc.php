            <h2>Arendal</h2>
          </header>
          <span class="image centered"><img src="images/arendal.jpg" /></span>
					<p>
						<strong>The Nordic Aluminium Casthouse Conference has been held in Arendal since 2006.</strong>
					</p>
					<p><strong>
						Arendal was established in the 14th century. It was known as "Outport Merd&oslash;" and was mainly a maritime loading place for timber. It was later named Arnadalr which means Eagle Valley in Old
						Norse, and received full town charter in 1735. It is thought that Arendal developed into one of the wealthiest towns in Northern Europe, mainly due to its profitable shipping, forestry and mining
						industries.</strong>
					</p>
					<p><strong>
						The town's golden era is represented by Tyholmen, the old part of Arendal. Arendal Old Town Hall is one of the largest remaining wooden houses in Northern Europe, and was the first house in the
						region to be designed by an architect. The traditional NorCast Dinner is held in this building. The formal rooms were last modernised in 1891, and have been maintained in the same style since. They
						house one of the largest collections of portrait paintings in Norway, and was started in 1889.</strong>
					</p>
					<p><strong>
						Arendal has developed from a dormant summer-town, attractive to recreational boaters and holiday makers, to a thriving all year town with festivals, events and a wide range of activities. This is,
						to some extent, due to the increased number of local businesses emerging in the international arena.</strong>
					</p>
